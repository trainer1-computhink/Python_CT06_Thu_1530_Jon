# POSSIBLE CHINESE NEW YEAR PYTHON PROBLEM SUMS

# ---

# 1. Firecracker Stall

# A firecracker stall sells boxes at $3.75 each.
# For every additional box after the first, the shop charges $0.50 extra handling fee.

# The stall owner enters how many boxes a customer buys.

# The program prints the final amount the customer must pay as a dollar value.

# ---

# 2. Ang Bao Sharing

# A grandparent prepares a total amount of money to be shared equally among 5 grandchildren.

# The total amount is entered into the program.

# The program displays how much each child receives and how much money is left over.
# The values shown must always make sense regardless of the amount entered.

# ---

# 3. Dragon Dance Space

# A round stage is prepared for a dragon dance performance.

# The organiser enters the radius of the stage.

# The program displays how much space the dragon dance occupies, shown as a clean and readable number.

# ---

# 4. Lantern Pole

# Lanterns are hung on a tall pole.
# Each lantern requires 0.8 metres of space.

# The height of the pole is entered.

# The program determines how many lanterns can be hung so that the pole is fully used without cutting any lantern.

# ---

# 5. Mandarin Orange Sale

# A customer buys mandarin oranges for Chinese New Year.

# The total number of oranges follows this pattern:
# number entered multiplied by (3 + 2)

# Each box can hold 8 oranges.

# The program displays how many full boxes can be packed.

# ---

# 6. Reunion Dinner Payment

# A reunion dinner bill is shared among family members.

# The total cost and the number of people are entered.

# The program displays how much each person pays and whether the total collected is more or less than the actual bill.

# ---

# 7. Fireworks Countdown

# Before midnight, a countdown to the fireworks is displayed.

# The number of minutes left is entered.

# The program prints one complete festive sentence that includes the countdown time and a Chinese New Year greeting.

# ---

# 8. Red Packet Delivery

# Each red packet contains money and decorations.
# The money weighs 12 grams and the decoration weighs 3 grams.

# The number of red packets is entered.

# Delivery boxes can carry up to 100 grams.

# The program determines how many boxes are needed so that no box exceeds the weight limit.

# ---

# 9. Dragon Costume Fabric

# Circular fabric pieces are cut for a dragon costume.

# The radius of one fabric piece and the total available fabric area are entered.

# The program displays how many complete fabric pieces can be made.

# ---

# 10. Lantern Festival Game Score

# During a Lantern Festival game, four numbers are entered.

# The final score depends on:
# the difference between two values,
# a circular calculation,
# and dividing and adjusting values before adding or subtracting.

# The program displays the final score clearly for the player.

# ---
